# Practice with the str.join() function

hyphen = '-'
colours = ('blue', 'red', 'yellow', 'orange')

print(hyphen.join(colours)) # blue-red-yellow-orange

space = ' '
days = ('mon', 'tue', 'wed', 'thur', 'fri')
print(space.join(days)) # mon tue wed thur fri

dot = '.'
vowels = ('a', 'e', 'i', 'o', 'u')
print(dot.join(vowels)) # a.e.i.o.ugit




